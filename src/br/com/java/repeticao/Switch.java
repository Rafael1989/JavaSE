package br.com.java.repeticao;

public class Switch {

	public static void main(String[] args) {
		int i = 6;
		switch(i){
			case 1:
				System.out.println(i);
				break;
			case 2:
				System.out.println(i);
				break;
			case 6:
				System.out.println(i);
				break;
			default:
				System.out.println(i);
				break;
		
		}
	}
}
